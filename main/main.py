from zeep import Client

client = Client('http://localhost:7777/ws/AcademicoWebService?wsdl')

def soap_listas(servicio):
    return{
        "estudiantes":client.service.getAllEstudiante()
    }[servicio]

def soap_individual(servicio, parametro):
    return{
        "asignatura":client.service.getAsignatura(parametro),
        "profesor":client.service.getProfesor(parametro)
    }[servicio]

print ("===================================================================================")
print ("==============================*LISTADO DE ESTUDIANTES*=============================")
print (soap_listas("estudiantes"))
print ("===================================================================================")
print ("===================================================================================")

print ("\n\n+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n\n")

print ("===================================================================================")
print ("================================*BUSCAR ASIGNATURA*================================")
print (soap_individual("asignatura", 1))
print ("===================================================================================")
print ("===================================================================================")

print ("\n\n+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n\n")

print ("===================================================================================")
print ("=================================*BUSCAR PROFESOR*=================================")
print (soap_individual("profesor", 1))
print ("===================================================================================")
print ("===================================================================================")